public class ValorFinalDaSoma {
    public static void main(String[] args) {
        

        int Indice = 12;
        int SOMA = 0;
        int K = 1;

        while(K < Indice){
            K = K + 1;
            SOMA = SOMA + K;
        }
        System.out.println(SOMA);
    }
}
